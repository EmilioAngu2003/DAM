        DatagramPacket packet = new DatagramPacket(buffer, buffer.length, grupo, puerto);
        try {
            jtaResultados.append("Servidor enviando a: "+packet.getSocketAddress()+"\n");
            socket.send(packet);
        } catch (IOException ex) {
            Logger.getLogger(ServidorMulticastGrafico.class.getName()).log(Level.SEVERE, null, ex);
        }
