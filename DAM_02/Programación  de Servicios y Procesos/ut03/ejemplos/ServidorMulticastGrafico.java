    class Hilo extends Thread{
        public void run(){
            
            while (true){
                
                byte [] buffer = new byte[1024];
                DatagramPacket packet = new DatagramPacket(buffer, buffer.length, grupo, puerto);
                try {
                    socket.joinGroup(grupo);
                    socket.receive(packet);
                } catch (IOException ex) {
                    Logger.getLogger(ServidorMulticastGrafico.class.getName()).log(Level.SEVERE, null, ex);
                }
                String res = new String(buffer).trim();
                if (res.length()>0 )
                jtaResultados.append(res+"\n");
            }
        }
    }

