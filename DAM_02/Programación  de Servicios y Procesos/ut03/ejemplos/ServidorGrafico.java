    class Conexion extends Thread{
        Socket s;
        Conexion (Socket s){
           this.s=s;
          
        }
        public void run(){
            try {
                pw= new DataOutputStream(s.getOutputStream());
                br=new DataInputStream(s.getInputStream());  
                String m;
                while (s.isConnected()){
                    m=br.readUTF();
                    System.out.println(m);
                    if (m!=null)
                        a単adeMensaje(m);
                    pw.writeUTF("OK");
                    
                    //taMensajesServidor.update(this.getGraphics());
                }
            } catch (IOException ex) {
                Logger.getLogger(ServidorGrafico.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    
    
    
    
    
    
    
    public void creaServidor(){
        String m;
        try {
            ss = new ServerSocket();
            InetSocketAddress direccion = new InetSocketAddress("localhost",Integer.parseInt(txtPuerto.getText()));
            ss.bind(direccion);
            a単adeMensaje("Servidor iniciado");
            btIniciar.enableInputMethods(false);
            while (ss.isBound()){
                s=ss.accept();
                a単adeMensaje("Cliente conectado " + s.toString());
                new GestionServicio(s);
            }
            
        } catch (Exception ex) {
            Logger.getLogger(ServidorGrafico.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
