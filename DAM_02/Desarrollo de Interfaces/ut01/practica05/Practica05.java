    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String directorio = System.getProperty("user.dir") + "/src/ut01/practica05/img";
        
        JFileChooser explorador = new JFileChooser(directorio);
        
        explorador.setFileFilter(new FileNameExtensionFilter("*.JPG", "jpg"));
        
        if( explorador.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            
            String path = explorador.getSelectedFile().getPath();
            
            System.out.println(path);
            
            Image img = new ImageIcon(path).getImage();
            
            ImageIcon icon = new ImageIcon(img.getScaledInstance(jLabelImg.getWidth(), jLabelImg.getHeight(), Image.SCALE_SMOOTH));
            
            jLabelImg.setIcon(icon);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

